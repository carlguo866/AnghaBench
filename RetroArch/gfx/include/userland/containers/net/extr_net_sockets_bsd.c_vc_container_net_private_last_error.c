   case 150: return VC_CONTAINER_NET_ERROR_IN_PROGRESS;
   case 152: return VC_CONTAINER_NET_ERROR_IN_USE;
   case 151: return VC_CONTAINER_NET_ERROR_INVALID_PARAMETER;
   case 137: return VC_CONTAINER_NET_ERROR_NETWORK;
   case 135: return VC_CONTAINER_NET_ERROR_NETWORK;
   case 136: return VC_CONTAINER_NET_ERROR_CONNECTION_LOST;
   case 149: return VC_CONTAINER_NET_ERROR_CONNECTION_LOST;
   case 147: return VC_CONTAINER_NET_ERROR_CONNECTION_LOST;
   case 134: return VC_CONTAINER_NET_ERROR_NO_MEMORY;
   case 133: return VC_CONTAINER_NET_ERROR_NOT_CONNECTED;
   case 132: return VC_CONTAINER_NET_ERROR_CONNECTION_LOST;
   case 130: return VC_CONTAINER_NET_ERROR_TIMED_OUT;
   case 148: return VC_CONTAINER_NET_ERROR_CONNECTION_REFUSED;
   case 140: return VC_CONTAINER_NET_ERROR_INVALID_PARAMETER;
   case 138: return VC_CONTAINER_NET_ERROR_INVALID_PARAMETER;
