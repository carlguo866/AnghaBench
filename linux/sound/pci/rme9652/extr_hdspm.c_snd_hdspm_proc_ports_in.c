struct snd_info_entry {struct hdspm* private_data; } ;
struct snd_info_buffer {int dummy; } ;
struct hdspm {int max_channels_in; int * port_names_in; } ;


 int snd_iprintf (struct snd_info_buffer*,char*,...) ;

__attribute__((used)) static void snd_hdspm_proc_ports_in(struct snd_info_entry *entry,
     struct snd_info_buffer *buffer)
{
 struct hdspm *hdspm = entry->private_data;
 int i;

 snd_iprintf(buffer, "# generated by hdspm\n");

 for (i = 0; i < hdspm->max_channels_in; i++) {
  snd_iprintf(buffer, "%d=%s\n", i+1, hdspm->port_names_in[i]);
 }
}
